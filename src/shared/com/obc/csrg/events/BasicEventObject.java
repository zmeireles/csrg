package com.obc.csrg.events;

import java.util.EventObject;

public class BasicEventObject extends EventObject {
	private final Object eventObject;
	
	private static final long serialVersionUID = 201001170340L;
	
	public BasicEventObject(final Object newEventObject){
		super(newEventObject);
		eventObject = newEventObject;
	}

	public Object getMyObject(){
		return eventObject;
	}
}
