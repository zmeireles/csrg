package com.obc.csrg.util;

import java.io.*;

public class FileOnTheFly {

	private String filename = "";
	private InputStream stream;
	private int size=0;
	
	public FileOnTheFly(String filename,InputStream stream){
		this.filename = filename;
		this.stream = stream;
	}
	public FileOnTheFly(String filename,InputStream stream,int size){
		this.filename = filename;
		this.stream = stream;
		this.size = size;
	}
	public String getFilename() {
		return filename;
	}
	public void setFilename(String filename) {
		this.filename = filename;
	}
	public InputStream getStream() {
		return stream;
	}
	public void setStream(InputStream stream) {
		this.stream = stream;
	}
	public int getSize() {
		return size;
	}
	public void setSize(int size) {
		this.size = size;
	}
	
	
}
