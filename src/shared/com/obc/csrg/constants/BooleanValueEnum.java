package com.obc.csrg.constants;

public enum BooleanValueEnum {
	INDIFFERENT,YES,NO;
	
	public String getName(){
		switch(this.ordinal()){
		case 0:return Constants.BOOLEAN_VALUE_INDIFFERENT;
		case 1:return Constants.BOOLEAN_VALUE_YES;
		case 2:return Constants.BOOLEAN_VALUE_NO;
		default :return Constants.BOOLEAN_VALUE_INDIFFERENT;
		}
	}

}
