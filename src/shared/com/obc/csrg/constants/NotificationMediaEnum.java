package com.obc.csrg.constants;

public enum NotificationMediaEnum {
	none,email,sms,news,fax;
	
	public String getName(){
		
		switch(this.ordinal()){
		case 0:return Constants.NOTIFICATION_NONE;
		case 1:return Constants.NOTIFICATION_EMAIL;
		case 2:return Constants.NOTIFICATION_SMS;
		case 3:return Constants.NOTIFICATION_NEWS;
		case 4:return Constants.NOTIFICATION_FAX;
		default :return Constants.NOTIFICATION_EMAIL;
		}
	}
}
