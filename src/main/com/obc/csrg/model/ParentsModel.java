package com.obc.csrg.model;

public abstract class ParentsModel<T extends Model> extends Model {
	
	private T desc;
	private T asc;
	
	public T getDesc() {
		return desc;
	}
	public void setDesc(T desc) {
		this.desc = desc;
	}
	public T getAsc() {
		return asc;
	}
	public void setAsc(T asc) {
		this.asc = asc;
	}
	
}
